    private void showCombobox() {
        if (nvBUS.list == null) {
            cvBUS.loadCbb();
        }
        for (String s : cvBUS.list) {
            cbb_chucVu.addItem(s);
        }

    }

    public void showTableNV() {
        Vector header = new Vector();
        header.add("Mã nhân viên");
        header.add("Mã chức vụ");
        header.add("Họ tên");
        header.add("Giới tính");
        header.add("Ngày sinh");
        header.add("Địa chỉ");
        header.add("Số điện thoại");
        header.add("Email");
        header.add("Lương");
        if (model.getRowCount() == 0) {
            model = new DefaultTableModel(header, 0);
        }
        for (NhanVien nv : nvBUS.dsnv) {
            model.addRow(new Object[]{
                nv.getMaNv(), nv.getMaCv(), nv.getHoTen(), nv.getGender(), nv.getDob(), nv.getDiaChi(), nv.getSdt(), nv.getEmail(), nv.getLuong()
            });
        }
        tblnv.setModel(model);
    }

    public void showTableCV() {
        Vector header = new Vector();
        header.add("Mã chức vụ");
        header.add("Chức vụ");
        header.add("Lương");
        if (model1.getRowCount() == 0) {
            model1 = new DefaultTableModel(header, 0);
        }
        for (ChucVu cv : cvBUS.dscv) {
            model1.addRow(new Object[]{
                cv.getMaChucVu(), cv.getChucVu(), cv.getLuong()
            });
        }
        tblcv.setModel(model1);
    }

