    private void tblnvMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblnvMouseClicked
        // TODO add your handling code here:
        int i = tblnv.getSelectedRow();
        NhanVien nv = nvBUS.dsnv.get(i);
        txt_maNv.setText(nv.getMaNv());
        txt_hoTen.setText(nv.getHoTen());
        cbb_chucVu.setSelectedItem(model.getValueAt(i, 1));
        String sex = model.getValueAt(i, 3).toString();
        switch (sex) {
            case "Nam":
                cbb_gioiTinh.setSelectedIndex(0);
                break;
            case "Nữ":
                cbb_gioiTinh.setSelectedIndex(1);
                break;
        }
        try {
            Date date = new SimpleDateFormat("yyyy-MM-dd").parse((String) model.getValueAt(i, 4));
            jDateChooser1.setDate(date);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, e);
        }

        txt_diaChi.setText(nv.getDiaChi());
        txt_sdt.setText(nv.getSdt());
        txt_mail.setText(nv.getEmail());
        txt_luong.setText(nv.getLuong().toString());
    }//GEN-LAST:event_tblnvMouseClicked

